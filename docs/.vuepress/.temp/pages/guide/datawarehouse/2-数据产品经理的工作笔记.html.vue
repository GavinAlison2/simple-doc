<h3 id="技术选型" tabindex="-1"><a class="header-anchor" href="#技术选型"><span>技术选型</span></a></h3>
<ul>
<li>
<p>Hadoop仍然是我们最常见的开源分布式架构。<br>
Hadoop设计的核心有两个：HDFS和MapReduce。<br>
通常需要搭配采集框架（例如比较流行的Kafka+Flume）、Hbase数据库、部署配置工具（Ambari）等一系列组件形成一个完整的架构。<br>
HDFS（Hadoop Distributed File System，分布式文件系统）既然是一个“系统”，就会包含一系列的功能和流程。主要由主服务器（NameNode）和负责存储数据的DataNode组成，NameNode负责管理文件系统的命名空间和客户端对文件的访问操作。<br>
MapReduce的本质是一种编程模型，或者说是一种计算方法。作为一个平台组件，它是用并行的方式处理大规模的计算。</p>
</li>
<li>
<p>Flume像个情报员，负责把情报（日志）拿到手交给负责接头的信息员Kafka运送回来，处长HDFS负责接收这些数据，指挥NameNode给它们起好名字并贴上标签，交给档案馆Hbase存起来；</p>
</li>
<li>
<p>这些数据还可以通过情报分析站MapReduce去做复杂的深度加工，加工好的结论也可以交给Hbase存起来。</p>
</li>
<li>
<p>而想要使用MapReduce需要通过工具Hive去实现。虽然Hbase是个超大的档案馆，MapReduce也能为这个超大的档案馆提供更多结果，但若是急需这些情报采取行动（需要实时输出这些数据），它们的效率就显得有点慢。</p>
</li>
<li>
<p>这时Kafka可以使用应急流程，把数据直接交给独立调查员Storm做实时分析，分析好了交给临时档案馆Redis存起来，领导（前端页面）可以去找Redis查阅结果。</p>
</li>
<li>
<p>可惜的是，Storm和Redis虽然效率很高，却没有Hbase这个档案馆那么庞大和稳定，工资（成本）又太高，至今只能打一打下手，做一些补充工作。</p>
</li>
</ul>
