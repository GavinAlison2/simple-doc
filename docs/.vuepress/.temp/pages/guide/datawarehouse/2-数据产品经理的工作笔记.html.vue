<h2 id="数据建模-核心思想是-分类" tabindex="-1"><a class="header-anchor" href="#数据建模-核心思想是-分类"><span>数据建模：核心思想是“分类”</span></a></h2>
<ul>
<li>分类逻辑可以分为横向和纵向两种类型。</li>
<li>横向分的是层次，纵向分的是主题（或者主体）。</li>
<li>这两个方向的分类构成了当前市场上数据仓库的主流分类逻辑。</li>
<li>按照数据加工的深度区分“层次”，基于的是相对通用的数据加工步骤。</li>
<li>纵向区分“主题”，是基于关键索引和指标体系的。</li>
<li>例如用户，会对应一个用户ID，一系列针对用户的宽表和描述用户的指标计算，这些内容放在一起就会形成一个“用户主题”。</li>
<li>电商网站会有“订单”主题，资讯类网站会有“内容”主题，平台会有“商家”主题，等等。</li>
</ul>
